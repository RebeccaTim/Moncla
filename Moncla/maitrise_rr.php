        <article><br><br>
        Notre espace routier est en perpétuelle mutation, la règlementation et la technologie automobile évolue à vitesse V … cette avancée, aussi prompt soit elle, peut rapidement nous dépasser au détriment de notre sécurité.
Je vous accompagne pour répondre à vos besoins de formation liés aux risques routiers. Je mets un point d’honneur à dédramatiser les situations pédagogiques utilisées et à mettre du sens à l’action.
« Ce que j’entends, je l’oublie - Ce que je vois, je le retiens - Ce que je fais, je le comprends »
Notre formation initiale de conducteur nous a enseigné les fondamentaux pour déplacer un véhicule lorsque tout se passe bien. Nos expérimentations, notre sensibilité, notre degré d’appétence pour la conduite et notre entourage ont fait de nous des conducteurs aux profils parfois antagonistes. 
Au travers de modules pédagogiques, je vous accompagne sur une formation dynamique où la notion de morale est proscrite au profit du sens, de la réalité de l’acte de conduire et de la compréhension de la règlementation en vigueur :
Partage de données réelles sur les limites physiologiques du conducteur et physiques d’un véhicule en mouvement
Expérimentation des réalités de la conduite sur un espace sécurisé et/ou simulateur (temps de réaction, distance freinage…)
Échanges sur la technologie automobile embarquée (ADAS, motorisation, astuces…)
Mise en situation : freinage d’urgence, évitement, perte d’adhérence
Accompagnement personnalisé
Professionnels, jeunes conducteurs, séniors, quel que soit votre dénomination, nous évoluons tous sur le même espace au quotidien. Ma mission est de vous accompagner pour plus de sérénité et de maîtrise dans votre trajet.
